-----------------------------------------------------------------------------------------------------------------------	
	-------------------------------------------------------------------------------------------------------------------
	Condition_QuestDestroyCastle = function()
	-------------------------------------------------------------------------------------------------------------------
				
		local Castle = Logic.GetEntityIDByName("CountessCastle")

		-- won if castle is burning or destroyed
		if 	IsDead("CountessCastle") or 
			Logic.GetEntityHealth(Castle) < (Logic.GetEntityMaxHealth(Castle) * 0.4) then 
			
			return true
			
		end
			
		return false

	end


		
	-------------------------------------------------------------------------------------------------------------------
	Action_QuestDestroyCastle = function()
	-------------------------------------------------------------------------------------------------------------------
		
		end1stQuest()
		
		return true
		
	end
