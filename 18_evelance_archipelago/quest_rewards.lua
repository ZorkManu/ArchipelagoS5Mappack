

REWARD1 = "KerberosDefenseOP"
REWARD2 = "Prison"
REWARD3 = "Reward3"
---

createQuestRewards = function()

	rewardPosition1 = GetPosition(REWARD1)
	rewardPosition2 = GetPosition(REWARD2)
	rewardPosition3 = GetPosition(REWARD3)

	StartJob("Reward1")
	StartJob("Reward2")
	StartJob("Reward3")

end



	
-----------------------------------------------------------------------------------------------------------------------	
--
--	JOB: "Reward1"
--
-----------------------------------------------------------------------------------------------------------------------	
	-------------------------------------------------------------------------------------------------------------------
	Condition_Reward1 = function()
	-------------------------------------------------------------------------------------------------------------------
					
		return IsDead(REWARD1) 
		
	end
		
	-------------------------------------------------------------------------------------------------------------------
	Action_Reward1 = function()
	-------------------------------------------------------------------------------------------------------------------
	
		CreateGoldChest(rewardPosition1)
		
		return true
		
	end	

-----------------------------------------------------------------------------------------------------------------------	
--
--	JOB: "Reward2"
