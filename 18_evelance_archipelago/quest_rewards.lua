		return true
		
		end	

-----------------------------------------------------------------------------------------------------------------------	
--
--	JOB: "Reward3"
--
-----------------------------------------------------------------------------------------------------------------------	
	-------------------------------------------------------------------------------------------------------------------
	Condition_Reward3 = function()
	-------------------------------------------------------------------------------------------------------------------
					
		return IsDead(REWARD3) 
		
		end
		
	-------------------------------------------------------------------------------------------------------------------
	Action_Reward3 = function()
	-------------------------------------------------------------------------------------------------------------------
	
