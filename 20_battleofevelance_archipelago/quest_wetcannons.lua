	StartJob("QuestWetCannons")

	end


destroyQuestWetCannons = function()


	end

-----------------------------------------------------------------------------------------------------------------------	
--
--	JOB: "QuestWetCannons"
--
-----------------------------------------------------------------------------------------------------------------------	
	-------------------------------------------------------------------------------------------------------------------
	Condition_QuestWetCannons = function()
	-------------------------------------------------------------------------------------------------------------------

		return Logic.GetWeatherState() == 2
		
		end
		
	-------------------------------------------------------------------------------------------------------------------
	Action_QuestWetCannons = function()
	-------------------------------------------------------------------------------------------------------------------
		
		for i = 1,8,1 do
		
			local name = "cannon"..i

			if IsAlive(name) then
			
				if createBriefing == nil then
					
					createBriefing = 1
					
					createBriefingExplodingCannons(name)

					destroyQuestWetCannons()				
				
					end				
				
				end

			end
		
		return true
		
		end
