
createQuestDestroyOutpost = function()

	StartJob("QuestDestroyOutpost")

	end


-----------------------------------------------------------------------------------------------------------------------	
--
--	JOB: "QuestDestroyOutpost"
--
-----------------------------------------------------------------------------------------------------------------------	
	-------------------------------------------------------------------------------------------------------------------
	Condition_QuestDestroyOutpost = function()
	-------------------------------------------------------------------------------------------------------------------
				
		return IsDead("outpost")		
		
		end
		
	-------------------------------------------------------------------------------------------------------------------
	Action_QuestDestroyOutpost = function()
	-------------------------------------------------------------------------------------------------------------------
		
		Report("outpost destroyed")
		
		-- open gates to inner castle
		for i = 1, 9 do
			ReplaceEntity("InnerGate"..i, Entities.XD_WallStraightGate)
		end

		createBriefingGatesOpen()

		SpokenMessage(String.MainKey.."LionsGateOpen")
		
		endQuestDestroyOutpost()
		
		return true
		
		end
